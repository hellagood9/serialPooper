function setMap(map, blockSize) {
  let col = 0;
  let row = 0;
  let columns = 0;
  let rows = 0;

  wall.length = 0;
  lava.length = 0;

  for (row = 0, rows = map.length; row < rows; row ++) {
    for (col = 0, columns = map[row].length; col < columns; col ++) {
      if (map[row][col] === 1) {
        wall.push(
          new Rectangle(
            col * blockSize,
            row * blockSize,
            blockSize,
            blockSize,
            true
          )
        );
      } else if (map[row][col] === 2) {
        lava.push(
          new Rectangle(
            col * blockSize,
            row * blockSize,
            blockSize,
            blockSize,
            true
          )
        );
      }
    }
  }
}
